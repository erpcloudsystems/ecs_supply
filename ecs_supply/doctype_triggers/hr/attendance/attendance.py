@frappe.whitelist()
def on_submit(doc, method=None):
    employee = frappe.get_doc("Employee", doc.employee)
    log = employee.append("attendance_logs", {})
    log.attendance_date = doc.attendance_date
    log.notes = doc.notes
    log.status = doc.status
    log.save()


